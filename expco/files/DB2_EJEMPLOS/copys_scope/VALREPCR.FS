******* VALREPCR-FS.v01.0001
******* CONSECUTVOS DE CAMPOS A REPORTAR POR SUSCRIPTOR
******* PROCESO DE VALIDACION PARA CARTERA Y CARJETAS
      $SET CONSTANT SW-STATUS-VALREPCR"NO"
           SELECT VALREPCR
                  ASSIGN TO DISK NOMBRE-VALREPCR
                  ORGANIZATION IS INDEXED
                  ACCESS MODE IS DYNAMIC
                  RECORD KEY IS LLAVE-VALREPCR
      $IF SW-STATUS-VALREPCR = "NO" 
      $DISPLAY ".............COMPILANDO VALREPCR SIN STATUS.........."
      $ELSE
      $DISPLAY ".............COMPILANDO VALREPCR CON STATUS.........."
                  FILE STATUS IS COD-RETORNO-VALREPCR
      $END
                  LOCK MODE IS MANUAL WITH LOCK ON RECORD.
