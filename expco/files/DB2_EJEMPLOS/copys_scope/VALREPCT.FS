******* VALREPCT-FS.v01.0001
******* CONSECUTVOS DE CAMPOS A REPORTAR POR SUSCRIPTOR
******* PROCESO DE VALIDACION PARA CUENTA CORRIENTE
      $SET CONSTANT SW-STATUS-VALREPCT"NO"
           SELECT VALREPCT
                  ASSIGN TO DISK NOMBRE-VALREPCT
                  ORGANIZATION IS INDEXED
                  ACCESS MODE IS DYNAMIC
                  RECORD KEY IS LLAVE-VALREPCT
      $IF SW-STATUS-VALREPCT = "NO" 
      $DISPLAY ".............COMPILANDO VALREPCT SIN STATUS.........."
      $ELSE
      $DISPLAY ".............COMPILANDO VALREPCT CON STATUS.........."
                  FILE STATUS IS COD-RETORNO-VALREPCT
      $END
                  LOCK MODE IS MANUAL WITH LOCK ON RECORD.
