      * MAESTRO HISTORICO DE DIRECCIONES POR CADA CUENTA QUE HA SIDO 
      * REPORTADA Y CUYA DIRECCION SE HA MODIFICADO POR LOS DIFERENTES 
      * SUSCRIPTORES. ALMACENA EL REGISTRO COMO ESTABA ANTES DE LA 
      * MODIFICACION
       $SET IDXFORMAT"8"
      
      $SET CONSTANT SW-STATUS-BDUHDIR"NO"
           SELECT MAESTRO-BDUHDIR
                  ASSIGN TO DISK MAES-BDUHDIR-TITLE
                  ORGANIZATION IS INDEXED
                  ACCESS MODE IS DYNAMIC
                  RECORD KEY IS LLAVE-BDUHDIR       
      $IF SW-STATUS-BDUHDIR = "NO"
      $DISPLAY ".............COMPILANDO BDUHDIR SIN STATUS.........."
      $ELSE
      $DISPLAY ".............COMPILANDO BDUHDIR CON STATUS.........."
                  FILE STATUS IS EST-BDUHDIR
      $END
                  LOCK MODE IS MANUAL WITH LOCK ON RECORD.
       $SET IDXFORMAT"0"
